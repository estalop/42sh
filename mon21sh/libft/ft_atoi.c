#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	if (str[i] == '\033')
		return (0);
	while ((str[i] > 0 && str[i] <= 32) || str[i] == 127)
		i++;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (k != 0)
			k = k * 10;
		k = k + str[i] - 48;
		i++;
	}
	i--;
	while (str[i] >= 48 && str[i] < 58)
		i--;
	if (str[i] == '-')
		return (-k);
	return (k);
}
