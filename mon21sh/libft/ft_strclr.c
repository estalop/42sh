#include "libft.h"

void	ft_strclr(char *s)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			i++;
		}
		while (i > 0)
		{
			i--;
			s[i] = '\0';
		}
	}
}
