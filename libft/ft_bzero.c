#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*d;
	unsigned int	e;

	e = 0;
	d = (unsigned char*)s;
	while (e < n)
	{
		d[e] = 0;
		e++;
	}
}
