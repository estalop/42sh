#include "libft.h"

int	ft_strcnt(char *s, char c)
{
	int p;
	int q;

	p = 0;
	q = 0;
	if (!s)
		return (-1);
	while (s[p])
	{
		if (s[p] == c)
			q++;
		p++;
	}
	return (q);
}
